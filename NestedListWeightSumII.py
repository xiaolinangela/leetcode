class Solution:
    def depthSumInverse(self, nestedList: List[NestedInteger]) -> int:
        def max_depth(n):
            curr_depth = 1
            for i in n:
                if not i.isInteger():
                    curr_depth = max(curr_depth, max_depth(i.getList())+1)
            return curr_depth
        depth = max_depth(nestedList)

        def helper(n, depth):
            sum = 0
            for i in n:
                if i.isInteger():
                    sum += i.getInteger()*depth
                else:
                    sum += helper(i.getList(), depth-1)
            return sum
        result = helper(nestedList, depth)
        return result
